// TestLocateZhiFu 照搬 anthonylee/qimen 的 "值符落宫" 测试（20 组）。
// 参考：anthonylee-qimen/src/qimen/__tests__/QimenUtil.test.ts L158-181
// 固定地盘：["癸","丁","丙","乙","戊","己","庚","辛","壬"]（阳遁五局）
func TestLocateZhiFu(t *testing.T) {
	earth := [9]string{"癸", "丁", "丙", "乙", "戊", "己", "庚", "辛", "壬"}

	// 把 anthonylee 的宫位名（如 "中五宮"）映射到飞星序索引
	palaceNameToFei := map[string]int{
		"坎一宫": 0, "坤二宫": 1, "震三宫": 2, "巽四宫": 3, "中五宫": 4,
		"乾六宫": 5, "兑七宫": 6, "艮八宫": 7, "离九宫": 8,
	}

	tests := []struct {
		dungan   string
		hourGan  string
		wantStar string
		wantPal  int // 飞星序索引 0..8（中5 时寄坤2 = 1，rawIsMiddle 额外标记）
		wantRaw5 bool // 原始落中5
	}{
		// dungan=戊 系列（注意：戊在地盘 earth[4]=中5 → 寄坤2）
		// anthonylee 标注 "天禽" 值符星，落宫原值为"中五宮"等
		{"戊", "甲", "天禽", 1, true},  // 甲时取 dungan 宫 = 中5 → 寄坤2
		{"戊", "乙", "天禽", 3, false}, // 乙 in earth[3]=巽4
		{"戊", "丙", "天禽", 2, false}, // 丙 in earth[2]=震3
		{"戊", "丁", "天禽", 1, false}, // 丁 in earth[1]=坤2
		{"戊", "戊", "天禽", 1, true},  // 戊在中5 → 寄坤2
		{"戊", "己", "天禽", 5, false}, // 己 in earth[5]=乾6
		{"戊", "庚", "天禽", 6, false}, // 庚 in earth[6]=兑7
		{"戊", "辛", "天禽", 7, false}, // 辛 in earth[7]=艮8
		{"戊", "壬", "天禽", 8, false}, // 壬 in earth[8]=离9
		{"戊", "癸", "天禽", 0, false}, // 癸 in earth[0]=坎1

		// dungan=己 系列（己在 earth[5]=乾6）
		{"己", "甲", "天心", 5, false},  // 甲时取 dungan 宫
		{"己", "乙", "天心", 3, false},
		{"己", "丙", "天心", 2, false},
		{"己", "丁", "天心", 1, false},
		{"己", "戊", "天心", 1, true},   // 戊在中5 → 寄坤2
		{"己", "己", "天心", 5, false},
		{"己", "庚", "天心", 6, false},
		{"己", "辛", "天心", 7, false},
		{"己", "壬", "天心", 8, false},
		{"己", "癸", "天心", 0, false},
	}

	_ = palaceNameToFei // reserved if we want to compare textual names later

	for _, tc := range tests {
		gotStar, gotPal, gotRaw := LocateZhiFu(earth, tc.dungan, tc.hourGan)
		if gotStar != tc.wantStar || gotPal != tc.wantPal || gotRaw != tc.wantRaw5 {
			t.Errorf("LocateZhiFu(dungan=%s, hourGan=%s):\n  got  (%s, %d, rawMid=%v)\n  want (%s, %d, rawMid=%v)",
				tc.dungan, tc.hourGan, gotStar, gotPal, gotRaw, tc.wantStar, tc.wantPal, tc.wantRaw5)
		}
	}
}

