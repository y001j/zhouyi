// ============ 查询辅助 ============

// DetectDoorPalaceHits 扫描本盘 8 个非中5 宫，返回命中的"门加九宫"条目。
// 中5 本身门迫作为门加中宫处理（当调用方显式想要时）。
func DetectDoorPalaceHits(pan *Pan) []DoorPalaceApplication {
	if pan == nil {
		return nil
	}
	indexByKey := make(map[[2]int]DoorPalaceApplication, len(DoorPalaceTable))
	for _, e := range DoorPalaceTable {
		indexByKey[[2]int{e.DoorIdx, e.PalaceIdx}] = e
	}

	var hits []DoorPalaceApplication
	for i, c := range pan.Cells {
		if c.Door == "" {
			continue
		}
		door, ok := BamenIndex[c.Door]
		if !ok {
			continue
		}
		palaceIdx := i + 1 // 飞星索引 0..8 → 宫号 1..9
		if e, ok := indexByKey[[2]int{door, palaceIdx}]; ok {
			hits = append(hits, e)
		}
	}
	return hits
}

// AuspiceLabelShort 吉凶分 → 短标签
func AuspiceLabelShort(s int) string {
	switch {
	case s >= 2:
		return "大吉"
	case s == 1:
		return "吉"
	case s == -1:
		return "凶"
	case s <= -2:
		return "大凶"
	}
	return "平"
}
