package qimen

// FocusGuide 按问题类型返回奇门遁甲的"解局侧重"文字。
//
// 与周易的 FocusGuide 同接口但内容奇门化：提示 LLM 该围绕哪些"星/门/神/干/宫"断，
// 而不是周易的"爻位 / 卦辞"。
//
// 依据：《奇门宝鉴》九星/八门/八神/十干类神 + 张志春《神奇之门》实战经验。
//
// qType 用与主包 QuestionType 相同的字符串枚举值：
//
//	"career" / "wealth" / "relation" / "health" / "decision" / "timing" / "other"（或空）
func FocusGuide(qType string) string {
	switch qType {
	case "career":
		return "【取象侧重 · 事业】\n" +
			"- **用神优先**：开门（讨谋图望）、天心（决策谋略）、值符（贵人提携）；文书看天辅/景门、官位看开门。\n" +
			"- **日干代表求测者本人**，其落宫星门神定本人状态；**时干代表所问之职位/机会**，关键看门之吉凶与是否门迫。\n" +
			"- **宜看格局**：青龙返首、飞鸟跌穴、三奇得使、玉女守门皆主升迁得力；反之大格（庚加乙）、刑格主阻滞。\n" +
			"- **九星月令**：天心、天辅在月令得旺相者，决断力与文书力俱强；若休囚死则需等时。\n" +
			"- **方位择吉**：以开门、生门、值符所在宫之后天方位为用事方向。\n"

	case "wealth":
		return "【取象侧重 · 财运】\n" +
			"- **用神优先**：生门（求财第一门）、天任（稳重储粮）、六合（合财之神）、戊（正财天干）。\n" +
			"- **财旺财弱**：看生门落宫五行是否被宫所生（门得生则财来顺）、宫所克（门迫则财艰）；九星天任在月令旺相者得力。\n" +
			"- **忌神警觉**：玄武（暗耗盗贼）、九地/天空（虚诈空耗）、庚加壬（小格破财）、庚加乙（大格大破）、三奇入墓（财虚浮不实）。\n" +
			"- **宜看格局**：青龙返首（丙加旬首）主正财大进、飞鸟跌穴主意外之财；荧入太白（丙加庚）客胜主忌冒进。\n" +
			"- **方位择吉**：以生门、天任、青龙所在宫位之方位为求财方向。\n"

	case "relation":
		return "【取象侧重 · 感情/人际】\n" +
			"- **用神优先**：六合（媒合缔交）、太阴（阴佑情感）、天辅（文雅之情）；男看庚/日干，女看乙/时干。\n" +
			"- **和合之象**：日干宫与时干宫五行相生、六合临吉门、乙庚相合（同宫或相邻）皆主亲合。\n" +
			"- **分歧之象**：日干宫与时干宫相克、六合落凶宫、太阴被冲、白虎临两干之间皆主不和。\n" +
			"- **宜看格局**：玉女守门（丁奇与值使同宫）主天作之合；青龙逃走（乙加辛）、白虎猖狂（辛加乙）主破合。\n" +
			"- **休杜门**：休门主柔情和美、杜门主闭藏秘守；若在日时干宫皆有情意。\n"

	case "health":
		return "【取象侧重 · 健康】\n" +
			"- **病位类神**：天芮（病星之首，所临之宫即病位所在）、杜门/死门为忌（闭塞与丧门）。\n" +
			"- **医药类神**：天心（武曲主医药）、生门（生气所在，病得生则愈）、太常（药石）、值符（医者贵人）。\n" +
			"- **病邪来路**：白虎主刀伤血光/急病、腾蛇主惊恐怪异/虚火、玄武主阴私暗病。\n" +
			"- **日干为病者**，其落宫状态、所乘星（得旺相则体力强、休囚死则衰）、空亡入墓皆要看。\n" +
			"- **病性辨五行**：宫/门之五行定病性——坎主寒湿血、离主热炎心神、震主惊筋、巽主风气、艮主积滞、兑主口舌肺燥。\n" +
			"- **忌天芮得令**（月令旺相）叠加凶将——病势重；**喜生门临日干宫**——病有转机。\n"

	case "decision":
		return "【取象侧重 · 抉择】\n" +
			"- **用神优先**：天心（决断）、天辅（思虑）、值符（明师请教）、开门（果断可行）、杜门（停止不宜进）。\n" +
			"- **宜进宜止**：开门临日干宫/三奇得使/青龙返首 → 宜进；杜门死门临日干/格局受阻/日干入墓 → 宜止。\n" +
			"- **表里对照**：日干为己意，时干为对方/所决之事；两宫五行关系定进退之势。\n" +
			"- **阻滞之象**：九地/勾陈（拖延）、腾蛇（疑虑）、门迫（门被宫克）、空亡（虚浮无应）。\n" +
			"- **必须给出明确倾向**（不要模棱两可），并说明机会与代价；若盘面两相均势，以值符值使落宫为裁决。\n"

	case "timing":
		return "【取象侧重 · 时运】\n" +
			"- **大势吉凶**：三吉门（开休生）与三吉将（值符六合太阴）入三传/临日时干宫越多越顺；凶门凶将多聚则受阻。\n" +
			"- **阴阳遁**：阳遁主动势外显、阴遁主阴势内藏——择机要顺阴阳遁之势。\n" +
			"- **三奇分布**：乙丙丁落三吉门且不入墓不空亡 → 时运昌盛；三奇入墓或空亡 → 运势虚浮。\n" +
			"- **九星当令**：当月旺相之星所临之宫为得势之位；休囚死之星所临则运势受制。\n" +
			"- **命主（日干）状态**：日干落吉宫、乘旺星、配吉门吉神 → 自身运势在吉；反之需忍耐等时。\n"

	default:
		return "【取象侧重 · 通用】\n" +
			"- 综观九宫：值符所在定大势、值使所在定动向、日干代表求测者本人。\n" +
			"- 三奇（乙丙丁）落吉宫见吉门为吉；六仪落墓、击刑、空亡皆忌。\n" +
			"- 留意本局已自动命中的格局——这是最直接的吉凶指引。\n"
	}
}
