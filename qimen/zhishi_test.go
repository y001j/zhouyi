package qimen

import "testing"

// TestLocateZhiShi_GuShuGolden 值使门 golden test。
//
// 依据《御定奇门遁甲·奇门宝鉴》"直使加时法"原文：
//
//	「看用时之支，值于地盘何宫，即以天盘直使加于此宫。
//	 假如加一局甲子时，子于坎一宫，休门加子一宫，所谓『伏吟』者也。」
//
// 核心规则：
//  1. 值使门名 = 旬首六仪（遁干）在地盘所在宫的固定配门。
//     中5宫旬首（阳遁甲辰旬癸入中5等情况）寄坤2，配死门。
//  2. 值使落宫 = 时支在地盘对应的固定宫（由 ZhiToPalaceFei 查）。
//
// 这与 anthonylee/qimen 的"按时干走 N 步"算法**不同**——我们采用古籍说法。
func TestLocateZhiShi_GuShuGolden(t *testing.T) {
	// 我们准备一组覆盖各阳遁局 + 各旬首 + 各时支的 golden case。
	// 值使落宫的期望值由"时支 → 固定地盘宫"查 ZhiToPalaceFei 直接得到，
	// 所以这一批测试本质是在验证：
	//   (a) 值使门名正确（旬首在地盘的门）
	//   (b) 值使落宫 = 时支地盘宫（不管时干/阴阳遁/局数）

	type tc struct {
		name     string
		dun      string
		ju       int
		dungan   string // 旬首六仪
		hourZhi  string
		wantGate string
		wantPal  int // 飞星索引 0..8
	}
	tests := []tc{
		// ===== 奇门宝鉴原文直接引用的锚点 =====
		// 「加一局甲子时，子于坎一宫，休门加子一宫，所谓『伏吟』者也」
		{"阳一甲子时(伏吟)", "阳遁", 1, "戊", "子", "休门", 0},

		// ===== 阳遁一局 · 甲子旬 10 时辰验证（值使门同为休门） =====
		// 旬首戊在坎1 → 值使休门；时支各自对应固定地盘宫
		{"阳一乙丑时", "阳遁", 1, "戊", "丑", "休门", 7},  // 丑→艮8
		{"阳一丙寅时", "阳遁", 1, "戊", "寅", "休门", 7},  // 寅→艮8
		{"阳一丁卯时", "阳遁", 1, "戊", "卯", "休门", 2},  // 卯→震3
		{"阳一戊辰时", "阳遁", 1, "戊", "辰", "休门", 3},  // 辰→巽4
		{"阳一己巳时", "阳遁", 1, "戊", "巳", "休门", 3},  // 巳→巽4
		{"阳一庚午时", "阳遁", 1, "戊", "午", "休门", 8},  // 午→离9
		{"阳一辛未时", "阳遁", 1, "戊", "未", "休门", 1},  // 未→坤2
		{"阳一壬申时", "阳遁", 1, "戊", "申", "休门", 1},  // 申→坤2
		{"阳一癸酉时", "阳遁", 1, "戊", "酉", "休门", 6},  // 酉→兑7

		// ===== 阳遁二局 · 戊起坤2 · 值使门=死门（坤2配门） =====
		// LayEarthStems("阳遁", 2) = [乙,戊,己,庚,辛,壬,癸,丁,丙]
		// 戊在索引1（坤2）→ 值使死门
		{"阳二甲子时", "阳遁", 2, "戊", "子", "死门", 0},
		{"阳二丁卯时", "阳遁", 2, "戊", "卯", "死门", 2},
		{"阳二壬申时", "阳遁", 2, "戊", "申", "死门", 1},

		// ===== 阴遁九局 · 戊起离9 · 值使门=景门（离9配门） =====
		// LayEarthStems("阴遁", 9) = [乙,丙,丁,癸,壬,辛,庚,己,戊]
		// 戊在索引8（离9）→ 值使景门
		{"阴九甲子时", "阴遁", 9, "戊", "子", "景门", 0},
		{"阴九丙寅时", "阴遁", 9, "戊", "寅", "景门", 7},
		{"阴九庚午时", "阴遁", 9, "戊", "午", "景门", 8},

		// ===== 阳遁六局 · 戊寄中5 → 值使门寄坤2 = 死门 =====
		// LayEarthStems("阳遁", 6) = [壬,癸,丁,丙,乙,戊,己,庚,辛]
		// 戊在索引5（乾6）→ 值使开门（乾6配开）
		// 注：此局戊不在中5 → 正常；为了测试中5寄宫，需选用旬首落中5的局。
		// 实际阳遁五局下，戊在中5（index=4）→ 值使寄坤2的死门
		{"阳五甲子时(戊寄中5→坤2死门)", "阳遁", 5, "戊", "子", "死门", 0},
		{"阳五丁卯时", "阳遁", 5, "戊", "卯", "死门", 2},

		// ===== 不同旬首（甲戌旬己为遁干） =====
		// 阳遁一局地盘：[戊,己,庚,辛,壬,癸,丁,丙,乙]
		// 己在索引1（坤2）→ 值使死门
		{"阳一甲戌时", "阳遁", 1, "己", "戌", "死门", 5},  // 戌→乾6
		{"阳一乙亥时", "阳遁", 1, "己", "亥", "死门", 5},  // 亥→乾6

		// 阳遁一局，甲申旬庚为遁干 → 庚在震3 → 值使伤门
		{"阳一甲申时", "阳遁", 1, "庚", "申", "伤门", 1},  // 申→坤2

		// 阳遁一局，甲午旬辛 → 辛在巽4 → 值使杜门
		{"阳一甲午时", "阳遁", 1, "辛", "午", "杜门", 8},

		// 阳遁一局，甲辰旬壬 → 壬在中5 → 寄坤2 → 值使死门
		{"阳一甲辰时(壬寄中5)", "阳遁", 1, "壬", "辰", "死门", 3},

		// 阳遁一局，甲寅旬癸 → 癸在乾6 → 值使开门
		{"阳一甲寅时", "阳遁", 1, "癸", "寅", "开门", 7},
	}

	for _, tc := range tests {
		t.Run(tc.name, func(t *testing.T) {
			earth := LayEarthStems(tc.dun, tc.ju)
			gotGate, gotPal := LocateZhiShi(earth, tc.dungan, tc.hourZhi)
			if gotGate != tc.wantGate || gotPal != tc.wantPal {
				t.Errorf("LocateZhiShi(dun=%s, ju=%d, dungan=%s, hourZhi=%s):\n  got  (%s, %d)\n  want (%s, %d)",
					tc.dun, tc.ju, tc.dungan, tc.hourZhi, gotGate, gotPal, tc.wantGate, tc.wantPal)
			}
		})
	}
}

// TestLayDoors_Smoke 简单验证八门布盘的合理性：
//
//	(1) 值使门本身落在 zhiShiPalaceFei 指定的宫位；
//	(2) 所有 8 个非中5宫都被填上了不同的门；
//	(3) 中5宫为空。
func TestLayDoors_Smoke(t *testing.T) {
	earth := LayEarthStems("阳遁", 1)
	gate, palFei := LocateZhiShi(earth, "戊", "子") // 甲子时伏吟，休门加坎1
	doors := LayDoors(gate, palFei, "阳遁")

	// 值使门必须落在 palFei
	if doors[palFei] != gate {
		t.Errorf("值使门落位错误：palFei=%d, doors[palFei]=%q, gate=%q", palFei, doors[palFei], gate)
	}
	// 中5宫为空
	if doors[4] != "" {
		t.Errorf("中5宫应为空: got %q", doors[4])
	}
	// 8 个非中5宫的门无重复且覆盖全部八门
	seen := map[string]bool{}
	for i := 0; i < 9; i++ {
		if i == 4 {
			continue
		}
		if doors[i] == "" {
			t.Errorf("飞星索引 %d 宫缺门", i)
			continue
		}
		if seen[doors[i]] {
			t.Errorf("门重复: %q", doors[i])
		}
		seen[doors[i]] = true
	}
	if len(seen) != 8 {
		t.Errorf("门数 %d ≠ 8", len(seen))
	}
}
