// ResolveKeTi 根据发传法与盘面细节返回课体名。
//
// MVP 策略：以发传法为主，辅以 SanChuan.Note 中已标注的子类名。
func ResolveKeTi(pan *Pan) KeTi {
	m := pan.SanChuan.Method
	note := pan.SanChuan.Note

	// 优先使用 Note 中已记录的具体课体
	if note != "" {
		return KeTi{Name: note, Summary: ketiSummary(note, m)}
	}
	// 按发传法给默认名
	fallback := map[string]string{
		"贼克法": "贼克课", "比用法": "知一课", "涉害法": "涉害课",
		"遥克法": "遥克课", "昴星法": "昴星课", "别责法": "别责课",
		"八专法": "八专课", "伏吟法": "伏吟课", "返吟法": "返吟课",
	}
	name, ok := fallback[m]
	if !ok {
		name = m
	}
	return KeTi{Name: name, Summary: ketiSummary(name, m)}
}

func ketiSummary(name, method string) string {
	if s, ok := ketiSummaryTable[name]; ok {
		return s
	}
	if s, ok := methodSummaryTable[method]; ok {
		return s
	}
	return "——"
}

