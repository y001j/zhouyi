// Trigram 八卦
type Trigram struct {
	Name    string
	Symbol  string
	Nature  string
	Lines   [3]int // 0=阴, 1=阳
}

// Hexagram 六十四卦
type Hexagram struct {
	Number   int
	Name     string
	Symbol   string
	Upper    string // 上卦（外卦）
	Lower    string // 下卦（内卦）
	Judgment string // 卦辞
	Image    string // 象辞
	Lines    [6]Line
}

// Line 爻
type Line struct {
	Position int    // 1-6
	Type     string // 初/二/三/四/五/上, 九/六
	Text     string // 爻辞
}

