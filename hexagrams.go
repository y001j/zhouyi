// TrigramLines 用于从爻数组查找对应八卦名称
// lines[0..2] 分别是第1、2、3爻（0=阴，1=阳）
func FindTrigram(lines [3]int) string {
	for name, t := range Trigrams {
		if t.Lines == lines {
			return name
		}
	}
	return ""
}

// FindHexagram 根据上下卦名找到六十四卦
func FindHexagram(upper, lower string) *Hexagram {
	for i := range Hexagrams {
		if Hexagrams[i].Upper == upper && Hexagrams[i].Lower == lower {
			return &Hexagrams[i]
		}
	}
	return nil
}

// FindHexagramByLines 根据六爻数值（6/7/8/9）找到卦
// lines[0]=初爻 ... lines[5]=上爻
func FindHexagramByLines(lines [6]int) *Hexagram {
	var lower, upper [3]int
	for i := 0; i < 3; i++ {
		if lines[i] == 6 || lines[i] == 8 {
			lower[i] = 0 // 阴
		} else {
			lower[i] = 1 // 阳
		}
	}
	for i := 0; i < 3; i++ {
		if lines[i+3] == 6 || lines[i+3] == 8 {
			upper[i] = 0
		} else {
			upper[i] = 1
		}
	}
	lowerName := FindTrigram(lower)
	upperName := FindTrigram(upper)
	return FindHexagram(upperName, lowerName)
}

// FindChangedHexagram 根据变爻生成变卦
func FindChangedHexagram(lines [6]int) *Hexagram {
	var changed [6]int
	hasChange := false
	for i, v := range lines {
		if v == 6 {
			changed[i] = 7 // 老阴变少阳（已定，不再变）
			hasChange = true
		} else if v == 9 {
			changed[i] = 8 // 老阳变少阴（已定，不再变）
			hasChange = true
		} else {
			changed[i] = v
		}
	}
	if !hasChange {
		return nil
	}
	return FindHexagramByLines(changed)
}

// CountChangingLines 统计变爻数量及位置
func CountChangingLines(lines [6]int) []int {
	var positions []int
	for i, v := range lines {
		if v == 6 || v == 9 {
			positions = append(positions, i+1) // 返回1-6位置
		}
	}
	return positions
}
